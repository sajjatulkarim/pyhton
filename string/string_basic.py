# String is a set of alphabets or characters
# String is immutable (cannot be changed)   
# String is ordered (indexing and slicing can be done)
# String is a collection of characters enclosed in single quotes(' '), double quotes(" "), or triple quotes(''' ''' or """ """)
# String can contain letters, numbers, special characters, and spaces
# String can be created using str() function
# String can be concatenated using + operator   
# String can be repeated using * operator
# String can be accessed using indexing and slicing 
# String can be formatted using f-strings, format() method, and % operator
# String can be converted to other data types using int(), float(), list(), tuple(), set and dict() functions 
# String can be converted to upper case using upper() method
# String can be converted to lower case using lower() method
# String can be stripped of whitespace using strip() method
# String can be split into a list using split() method  
# String can be joined using join() method
# String can be searched using find() and index() methods
# String can be replaced using replace() method
# String can be checked for a substring using in operator
# String can be checked for alphanumeric using isalnum() method
# String can be checked for alphabetic using isalpha() method
# String can be checked for digit using isdigit() method
# String can be checked for lowercase using islower() method    
# String can be checked for uppercase using isupper() method
# String can be checked for whitespace using isspace() method
# String can be checked for title case using istitle() method
# String can be checked for printable using isprintable() method    
# String can be checked for startswith() and endswith() methods
# String can be iterated using for loop
# String can be reversed using slicing
# String can be measured for length using len() function
# String can be counted for occurrences of a substring using count() method
# String can be encoded using encode() method
# String can be decoded using decode() method
