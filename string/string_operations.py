# here we take care of string operations

str1 = "Hello"
str2 = "World"  

print(str1) # prints Hello
print(str2) # prints World

print (type(str1)) # prints <class 'str'> [Type function returns the type of the variable passed to it]
print (type(str2)) # prints <class 'str'>

# Concatenation  means joining two strings
str3 = str1 + " " + str2 # Concatenating str1 and str2 with a space in between here + 
print(str3) # prints Hello World    